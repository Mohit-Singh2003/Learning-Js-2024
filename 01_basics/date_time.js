// We will learn all the concepts of Date & Time in JavaScript.
let myDate = new Date();
console.log(myDate.toString()); 
//Mon Feb 26 2024 14:25:18 GMT+0000 (Coordinated Universal Time)
console.log(myDate.toDateString()); //Mon Feb 26 2024
console.log(myDate.toLocaleDateString()); //2/26/2024
console.log(myDate.toLocaleTimeString()); //2:28:38 PM
console.log(myDate.toTimeString()); 
//14:29:10 GMT+0000 (Coordinated Universal Time)
